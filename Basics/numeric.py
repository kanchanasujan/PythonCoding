num1 = 5.3
num2 = 2.4

#addition
result1 = num1+num2
print(result1)

#Subtraction
result2 = num1-num2
print(result2)

#Multiply
result3 = num1*num2
print(result3)
print(round(result3,2))

#Division
result4 = num1/num2
print(result4)
print(round(result4,3))

# Integer Division
result5 = num1 // num2
print("Integer Division:", result5)

# Modulus (Remainder)
result6 = num1 % num2
print("Modulus (Remainder):", result6)

# Absolute Value
result7 = abs(-7)
print("Absolute Value:", result7)
